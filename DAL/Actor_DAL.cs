				oDB.MySQLAddParameter(MySqlCmd, "p_idtipo_actor", p_idtipo_actor);
				oDB.MySQLAddParameter(MySqlCmd, "p_idpersona", p_idpersona);
				oDB.MySQLAddParameter(MySqlCmd, "p_idpersona_representante", p_idpersona_representante=="0"?null: p_idpersona_representante);
